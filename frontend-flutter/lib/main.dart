  final String title;

  @override
  Widget build(BuildContext context) {
    return Scaffold(
        appBar: AppBar(
          title: Text(title),
        ),
        body: Center(
          child:
              Text("Hello World", style: Theme.of(context).textTheme.headline4),
        ));
  }
}
